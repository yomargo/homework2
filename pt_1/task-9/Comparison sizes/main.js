var size = Number(prompt("Type your size here"))

if (size === 40) {
  alert("Your USA size is S")
} else if (size === 42) {
  alert("Your USA size is M")
} else if (size === 44) {
  alert("Your USA size is M")
} else if (size === 46) {
  alert("Your USA size is L")
} else if (size === 48) {
  alert("Your USA size is L")
} else if (size === 50) {
  alert("Your USA size is XL")
} else if (size === 52) {
  alert("Your USA size is XL")
} else if (size === 54) {
  alert("Your USA size is XXL")
} else {
  alert("Your size was not found, please try again")
}